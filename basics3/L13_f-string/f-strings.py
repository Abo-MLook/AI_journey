#An f-string is a way to embed expressions, variables,
# or even calculations directly inside a string using {}.



name = "Mrwan"
age = 21

# Old way
print("My name is {} and I am {} years old.".format(name, age))

# f-string way
print(f"My name is {name} and I am {age} years old.")


x = 10
print(f"{x=}")  # Shows: x=10
